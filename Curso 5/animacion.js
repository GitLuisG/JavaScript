var uno=1;
var dos=10;
/*
 * while
 * dp - while
 * for
 * break- continue
 * 
 */

while(uno < dos){
    document.write(uno + "<br>");
    uno++;
}

document.write("<br>");
uno = 0;

do{
    document.write(uno + "<br>");
    uno++;
}while(uno <= dos);

document.write("<br>");
uno = 0;

for(var i=0;i <= 10;i++){
    document.write(i,"<br>");
}
